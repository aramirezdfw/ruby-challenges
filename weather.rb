puts "You can still get a sunburn even if the sun is not out today."
when 'foggy'
puts "Use low bean headlights in the car."
when 'windy'
puts "Hang on to your hat!"
when 'stormy'
puts "Watch out for lightning!"
else
puts "Might as well stay home today!"
end
